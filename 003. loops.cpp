/* A loop is a series of instructions that are progressively 
repeated until a certain condition is met. */
#include<iostream>
using namespace std;

int main()
{
    /* For loop is an iterative statement that is used to
    execute a piece of code based on the validity of 
    certain condition */
    for(int i=0; i<=10; i++)
    {
        cout << i << endl;
    }

    return 0;
} 